package com.palfed.android.menu.activities.objects;

public class ItemObject {
	String isItem;
	String title;
	String titleRestaurant;
	String content;
	String day;
	String time;
	String portion;
	String comment;
	String eaten;
	String miles;
	String urlImv;

	public String getIsItem() {
		return isItem;
	}

	public void setIsItem(String isItem) {
		this.isItem = isItem;
	}

	public void setContent(String content) {
		this.content = content;
	}
	public String getContent() {
		return content;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTitle() {
		return title;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getDay() {
		return day;
	}
	public void setUrlImv(String urlImv) {
		this.urlImv = urlImv;
	}
	public String getUrlImv() {
		return urlImv;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getComment() {
		return comment;
	}

	public void setEaten(String eaten) {
		this.eaten = eaten;
	}

	public String getEaten() {
		return eaten;
	}
	public void setMiles(String miles) {
		this.miles = miles;
	}
	public String getMiles() {
		return miles;
	}

	public void setPortion(String portion) {
		this.portion = portion;
	}

	public String getPortion() {
		return portion;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getTime() {
		return time;
	}

	public void setTitleRestaurant(String titleRestaurant) {
		this.titleRestaurant = titleRestaurant;
	}

	public String getTitleRestaurant() {
		return titleRestaurant;
	}
}
